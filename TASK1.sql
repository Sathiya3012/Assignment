CREATE TABLE ENROLLMENT(
ENROLLMENT_ID INT PRIMARY KEY,
STUDENT_ID INT,
COURSE_ID INT,
FOREIGN KEY (STUDENT_ID) REFERENCES STUDENT(STUDENT_ID),
FOREIGN KEY(COURSE_ID) REFERENCES COURSE(COURSE_ID)
);

